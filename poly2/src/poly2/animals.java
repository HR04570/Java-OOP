package poly2;

/**
 *
 * @author hp
 */
public class animals {
    String color;
    int legs;
    String type;
    String sound;

    public animals(String color, int legs, String type) {
        this.color = color;
        this.legs = legs;
        this.type = type;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getLegs() {
        return legs;
    }

    public void setLegs(int legs) {
        this.legs = legs;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSound() {
        return sound;
    }

    public void setSound(String sound) {
        this.sound = sound;
    }
   
}
